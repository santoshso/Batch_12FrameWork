package test;

public class Datatypes {

	public static void main(String[] args) {
		
		
		// primitive ---size is fixed
		
		int a=10;
		System.out.println(a); // 10
		
		boolean b= true;
		System.out.println(b);// true
		
		char c= 'h';
		System.out.println(c);// h
		
		double d=12.5;
		System.out.println(d);// 12.5
		
		// non primitive ---size not fixed
		
		String name = "buggspotter testing classes";
		System.out.println(name);// bugspotter testing classes
		
		
		// by default value 
		
        int p = 0;
        System.out.println(p);
        
        
        float f = 0;
        System.out.println(f);
        
        boolean x = false;
        System.out.println(x);
		
	String id = null;
	System.out.println(id);
		
				

	}

}
