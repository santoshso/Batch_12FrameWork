package test;

public class Operators {

	public static void main(String[] args) {
		
		int a= 50;
		// Increment =====>  a++___post increment
		           //   =====++a ____pre increment
		
		System.out.println(a++);
		System.out.println(a);
		System.out.println(++a);
		
		
		
		// decrement ====> a--____post increment
		            //====--a_____pre increment
		
		
		System.out.println(a--);
		System.out.println(a);
		System.out.println();

	}

}
