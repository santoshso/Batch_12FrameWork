package inheritance;

public class palindromestring {

	public static void main(String[] args) {
		
	String s = "11122111";
	
	String rev ="";
	
	for (int i=s.length()-1;i>=0;i--);
	{
		
		rev=rev+s.charAt(0);
	}
	if (s.equals(rev))
	{
		System.out.println("palindrome string");
	}
	else
	{
		
	
	
	
		
		System.out.println("no palindrome string");
		
		
	
		
	
		
	}
	}
}

	


